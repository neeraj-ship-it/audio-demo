async function produceStory(template, index) {
  // Narration
  console.log('ЁЯОЩя╕П  Generating narration...');
  const narration = await generateNarration(template.script);

  // Upload
  console.log('тШБя╕П  Uploading...');
  const url = await uploadToS3(narration, `story-${Date.now()}-${index}`);

  // Save
  const story = {
    id: Date.now() + index,
    title: template.title,
    description: template.description,
    category: template.category,
    duration: '8-10 min',
    audioUrl: url,
    thumbnailUrl: getThumbnail(template.category),
    generated: true,
    new: true,
    emoji: getEmoji(template.category),
    rating: 0,
    ratingCount: 0,
    createdAt: new Date().toISOString()
  };

  await saveToDatabase(story);
  return story;
}

async function generateNarration(script) {
  const https = require('https');
  const data = JSON.stringify({
    text: script,
    model_id: 'eleven_multilingual_v2',
    voice_settings: { stability: 0.5, similarity_boost: 0.75 }
  });

  return new Promise((resolve, reject) => {
    const req = https.request({
      hostname: 'api.elevenlabs.io',
      path: '/v1/text-to-speech/21m00Tcm4TlvDq8ikWAM',
      method: 'POST',
      headers: {
        'Accept': 'audio/mpeg',
        'Content-Type': 'application/json',
        'xi-api-key': process.env.ELEVENLABS_API_KEY,
        'Content-Length': Buffer.byteLength(data)
      }
    }, (res) => {
      const chunks = [];
      res.on('data', chunk => chunks.push(chunk));
      res.on('end', () => resolve(Buffer.concat(chunks)));
    });
    req.on('error', reject);
    req.write(data);
    req.end();
  });
}

async function uploadToS3(buffer, filename) {
  const s3 = new AWS.S3({
    accessKeyId: process.env.AWS_ACCESS_KEY_ID,
    secretAccessKey: process.env.AWS_SECRET_ACCESS_KEY,
    region: process.env.AWS_REGION
  });

  const key = `audio/${Date.now()}-${filename}.mp3`;
  await s3.upload({
    Bucket: process.env.AWS_S3_BUCKET || 'stagefm-audio',
    Key: key,
    Body: buffer,
    ContentType: 'audio/mpeg'
  }).promise();

  return `https://${process.env.AWS_S3_BUCKET || 'stagefm-audio'}.s3.${process.env.AWS_REGION}.amazonaws.com/${key}`;
}

async function saveToDatabase(story) {
  const dataPath = path.join(__dirname, '..', 'data', 'stories.json');
  const data = JSON.parse(await fs.readFile(dataPath, 'utf-8'));

  if (Array.isArray(data)) {
    data.unshift(story);
  } else if (data.stories) {
    data.stories.unshift(story);
  }

  await fs.writeFile(dataPath, JSON.stringify(data, null, 2));
}

